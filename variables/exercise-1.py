# Define three variables var1, var2, and var3. Prompt the user to input three integers for the three variables.
# Calculate the average and store it in a variable average. Display the average.

var1 = int(input("Input number 1: "))
var2 = int(input("Input number 2: "))
var3 = int(input("Input number 3: "))

average = (var1 + var2 + var3) / 3

print("The average is: ", average)
