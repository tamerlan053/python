# Two numbers are entered via the keyboard. If the numbers are equal, the following is printed: "The two numbers are equal". Otherwise, the following is printed: "The two numbers are not equal".

number1 = int(input("Enter the first number: "))
number2 = int(input("Enter the second number: "))
if number1 == number2:
    print("The numbers are equal!")
else:
    print("The numbers are not equal!")
